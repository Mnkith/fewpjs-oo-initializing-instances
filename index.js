class Breakfast{
  constructor(food, drink){
  this.food = food
  this.drink = drink
  }
}

class Lunch {
  constructor(sal, so, dr){
    this.salad = sal
    this.soup = so
    this.drink = dr 
  }
}

class Dinner {
  constructor(sal, so, ent, des){
    this.salad = sal
    this.soup = so
    this.entree = ent
    this._dessert = des 
  }
}